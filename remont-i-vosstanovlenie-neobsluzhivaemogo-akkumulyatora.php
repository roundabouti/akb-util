                                        <p class="pt-2">
     Чтобы восстановить необслуживаемый аккумулятор, нужно точно знать причину и характер неисправности. Чаще всего определить это можно только при помощи специального оборудования.
</p>
<h2 class="text-danger-custom text-center text-xl-left">Что такое необслуживаемые АКБ</h2>
   <p class="pt-2">
     Одними из основных технических и эксплуатационных характеристик аккумуляторов являются плотность и объем (уровень) электролита. В обычных АКБ можно открутить пробку и провести замеры этих параметров, при необходимости – сделать доливку или полную замену жидкости внутри. В необслуживаемом аккумуляторе нет доступа к банкам, поэтому такие процедуры невозможны.
</p>
<h4 class="text-danger-custom text-center text-xl-left">Причины выхода из строя:
</h4>
<ul class="red">
    <li>сульфатация пластин, когда емкость снижается до минимума;</li>
    <li>механическое разрушение пластин;</li>
    <li>короткое замыкание, которое приводит к повышению температуры электролита;</li>
    <li>неправильное хранение и эксплуатация.</li>
</ul>
   <p class="pt-2">
 <h4 class="text-danger-custom text-center text-xl-left">Недостатки необслуживаемых АКБ:</h4>
</p>
<ul class="red">
    <li>Имеют высокую чувствительность к погоде. Уязвимы к сильным морозам.</li>
    <li>Энергетический потенциал батареи, необходимый для запуска двигателя, снижается при нехватке заряда, который происходит при пробеге на небольшое расстояние.</li>
    <li>Оставлять машину под открытым небом не рекомендуется.</li>
    <li>При морозных температурах электролит становится вязким. В нем падает скорость диффузии ионов.</li>
    <li>В момент запуска двигателя верхний слой жидкости прогревается, от нее поступает основная часть электроэнергии. В процессе прогрева в таких местах происходит активная сульфатация. Химические реакции выделяют сернокислый свинец поверх пластин. Частички металла создают препятствия для нормального заряда и движения энергии. Происходит снижение емкости батареи.&nbsp; На клеммах появляется налет.</li>
</ul>
<h2 class="text-danger-custom text-center text-xl-left">Возможно ли восстановление необслуживаемого аккумулятора</h2>
   <p class="pt-2">
     Восстановить такую АКБ можно только при наличии специального оборудования. Есть несколько технологий восстановления.
</p>
   <p class="pt-2">
     Необходимость проводить обслуживание аккумуляторов такого типа чаще всего вызвана падением объема электролита. Специфика работы таких АКБ состоит в том, что в процессе разрядки на аноде восстанавливается диоксид свинца серной кислоты. На катоде при этом свинец окисляется. При зарядке все эти химические процессы протекают в обратном направлении.
</p>
   <p class="pt-2">
     На корпусе необслуживаемой АКБ есть специальная шкала, при помощи которой можно следить за уровнем жидкости внутри. Стандартные методы проверки состояния аккумулятора тут неприменимы.
</p>
   <p class="pt-2">
     Если уровень жидкости снизился настолько, что пластины открылись и стали высыхать, то сразу же начнется активный процесс их разрушения – они станут сыпаться в виде порошка вниз.
</p>
   <p class="pt-2">
     Некоторые модели АКБ имеют специальные отверстия, предназначенные для залива дистиллированной воды. Ее нужно добавлять в таких объемах, которые нужно рассчитать на основании показателей индикатора или специального прибора. Если таких отверстий нет, то долить воду не получится. Ни в коем случае нельзя пытаться проделать такие отверстия самостоятельно! В этом случае герметичность устройства будет нарушена, что может привести к вытеканию или испарению жидкости.
</p>
   <p class="pt-2">
     Если АКБ имеет какие-то повреждения или иные дефекты, то восстановить его чаще всего невозможно. В специальных мастерских и сервисных центрах проводят десульфатацию пластин. Это делается при помощи специальных химических веществ. Такие способы «реанимации» позволяют увеличить срок службы аккумулятора.
</p>
   <p class="pt-2">
     Другая технология заключается в многократной зарядке и разрядке, проводимой током малой силы. Это повышает основные технические показатели батареи, в том числе плотность электролита.
</p>
   <p class="pt-2">
     Корпус изготавливается из пластика. Он подвержен механическому воздействию. При любом неосторожном движении могут появиться вмятины, трещины. Устранить такие дефекты можно при помощи специальной сварки.
</p>
   <p class="pt-2">
     Если на клеммах появился налет, то в некоторых случаях их можно восстановить. Это происходит из-за окислительных процессов, которые разъедают металл. При очистке нельзя использовать вазелин, т.к. он снижает проводимость, протирать клеммы бензином или соляркой. Чаще всего очистку делают при помощи соды.
</p>
<h4 class="text-danger-custom text-center text-xl-left">Порядок действий:
</h4>
<ol>
    <li>при неработающем двигателе ослабляют гайки, снимают клеммы;</li>
    <li>АКБ вынимают, проверяют на предмет наличия трещин и вмятин;</li>
    <li>при помощи старой зубной щетки наносят раствор на клеммы и тщательно протирают, обрабатывают защитной смазкой;</li>
    <li>ставят батарею на место и подсоединяют провода.</li>
</ol>
   <p class="pt-2">
     Если налет слишком плотный и затвердевший, то для очистки используют наждачную бумагу и острый нож. Но делать это надо очень аккуратно.
</p>
   <p class="pt-2">
     Перед проверкой работы аккумулятора нужно убедиться, что все детали и контакты нормально закреплены, чтобы не произошло короткого замыкания.
</p>
   <p class="pt-2">
     Чтобы необслуживаемый аккумулятор прослужил как можно дольше, рекомендуется регулярно проверять состояние электролита, его основные характеристики (плотность и уровень). Оставлять батарею зимой на улице или в холодном помещении не рекомендуется.
</p>
<h3 class="text-danger-custom text-center text-xl-left">Опасность восстановления своими руками</h3>
   <p class="pt-2">
     Если необслуживаемая батарея начала работать с перебоями или совсем вышла из строя, то ни в коем случае нельзя пытаться что-либо делать с ней самостоятельно. Это чревато не только окончательной поломкой батареи, но и мелкими чрезвычайными ситуациями. Может произойти выплеск жидкости, выделение газов, загрязнение окружающей среды, отравление человека или получение травм. Любые диагностические или ремонтно-восстановительные мероприятия можно проводить только в специальных мастерских или сервисных центрах, где профессиональные автоэлектрики при помощи оборудования могут сразу точно поставить диагноз, определить целесообразность дальнейшего ремонта. Чаще всего необслуживаемые аккумуляторы восстановлению не подлежат. Их нужно сдавать в пункты приема компаний, которые занимаются утилизацией старых батарей. В Москве это ООО «Стандарт».
            

