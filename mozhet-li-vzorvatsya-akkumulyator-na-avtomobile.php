     <p class="pt-2">
	 Несколько десятков лет назад, во времена массового производства обслуживаемых источников питания для авто, при зарядке аккумулятора водителям приходилось соблюдать определенные меры безопасности. Ведь при наличии открытых источников пламени, искр, либо вполне безобидных нагревательных элементов вблизи батареи мог произойти серьезный взрыв. А осколки пластикового корпуса и электролит, состоящий из агрессивных химических веществ, превращал АКБ в бомбу замедленного действия.&nbsp;
</p>
 
  <p class="pt-2">
	 Сегодня эта проблема решена лишь отчасти: в новостных сводках можно найти десятки статей о том, что при зарядке в гараже (а страшнее – при езде) взорвался аккумулятор. Поэтому мы решили рассказать, почему происходят такие недоразумения, а также дать инструкцию на случай, если вы столкнулись с этой проблемой!
 

</p>
<h2 class="text-danger-custom text-center text-xl-left">Почему взрываются автомобильные аккумуляторы</h2>
 <p class="pt-2">
	 Забегая наперед, стоит сказать, что взрыв АКБ – это не выдумка, а реальность. Но происходит это крайне редко, ведь для того, чтобы он произошел, необходимо создать правильные условия, что практически невозможно даже при злостном несоблюдении техники безопасности. С другой стороны, «хлопок» не так страшен, как может показаться на первый взгляд, т.к. не разнесет авто в щепки.
</p>
 <p class="pt-2">
	 Теперь разберемся, что может повлечь за собой «детонирование» батареи.
</p>
 <p class="pt-2">
	 По сути, любой автомобильный аккумулятор состоит из трех частей:
</p>
 <ul class="red">
	<li>Пластикового корпуса;</li>
	<li><a href="https://akb-util.ru/services/priem-svintsa/">Свинцовых пластин</a>;</li>
	<li>Электролита (смеси дистиллированной воды и серной кислоты).</li>
</ul>
 <p class="pt-2">
	 При этом ни металл, ни пластик сами по себе взорваться не могут. Да и H2O+H2SO4 – не взрывоопасная смесь. Но при «перезарядке» батареи, вода может расщепиться на свои основные составляющие – кислород и газообразный водород, именуемый «гремучим газом». При большом скоплении этого вещества в банках АКБ, малейшая искра может повлечь за собой вздутие корпуса (в лучшем случае) или «разнести» источник питания в клочья (худший вариант).
</p>
 <p class="pt-2">
	 Однако опытные автолюбители и работники СТО могут определить вероятность взрыва по косвенным признакам.
</p>
 <p class="pt-2">
	 Так, риск детонации увеличивается при:
</p>
 <ul class="red">
	<li>Систематических сбоях в работе регулятора напряжения. Повышает интенсивность газообразования, увеличивает риск проблем с АКБ в будущем, а сама проблема может проявить себя даже во время езды;</li>
	<li>Физических повреждениях, вздутии корпуса аккумулятора. Чаще всего – следствие отсутствия обязательного технического обслуживания;</li>
	<li>Низком уровне электролита. В отсутствие регулярного пополнения запаса токопроводящей жидкости, газ будет заполнять простор банки, следовательно – увеличит вероятность «хлопка»;</li>
	<li>Неграмотном утеплении&nbsp;батареи в зимний период. В отсутствие возможности для вывода образовавшихся газов, риск ЧП – максимальный.</li>
</ul>
 <p class="pt-2">
	 Для полноценного взрыва же достаточно:
</p>
 <ul class="red">
	<li>Неисправности автомобильной электропроводки;</li>
	<li>Непогашенной спички или сигареты около капота;</li>
	<li>Статического электричества (от синтетики и т.д.);</li>
	<li>Перенагрузки АКБ при подключении дополнительных устройств;</li>
	<li>Понижения уровня электролита в бачке.</li>
</ul>
 <p class="pt-2">
	 По уровню мощности, взрыв напоминает хлопок, издающийся при выстреле из крупнокалиберного стрелкового оружия.
</p>
<h2 class="text-danger-custom text-center text-xl-left">Что делать, если взорвалась АКБ</h2>
 <p class="pt-2">
	 Полноценный взрыв происходит редко, т.к. производители стараются обеспечить максимальный уровень безопасности. Да и международные контролирующие органы предъявляют жесткие требования к производству автомобильных батарей: ISO, ГОСТ – это не пустые звуки, а свод правил, разработанных за несколько десятилетий активного развития автомобилестроения.
</p>
 <p class="pt-2">
	 Но если он все-таки произошел, при этом вы и ваше авто остались целы, возникает логический вопрос: а что делать со взорвавшимся аккумулятором?
</p>
 <p class="pt-2">
	 Категорически запрещено:
</p>
 <ul class="red">
	<li>Пытаться «реанимировать» его с помощью подручных средств;</li>
	<li>Разбирать батарею в «кустарных» условиях без дополнительной защиты;</li>
	<li>Хранить поврежденное устройство в гараже/сарае/дома;</li>
	<li>Выбрасывать его на свалку бытовых отходов, улицу и т.д.</li>
</ul>
 <p class="pt-2">
	 Ведь электролиты, служащие средой для передачи энергии, крайне опасны, т.к. состоят из серной кислоты, несущей серьезную опасность не только для окружающей среды, а и для человека. Да и поврежденный пластик может стать причиной всевозможных неприятностей, вплоть до отравления воздуха в результате испарений.
</p>
 <p class="pt-2">
	 По закону, поврежденные АКБ подлежат обязательной утилизации, с соблюдением условий, установленных на законодательном уровне. При этом в России есть компании, готовые купить испорченную батарею с целью дальнейшей переработки. Так почему бы не заработать на том, что и так «нежизнеспособно» и приносит один лишь вред?
</p>
 <p class="pt-2">
	 Занимаясь скупкой поврежденных, отработанных и проблемных аккумуляторных батарей для авто-, мото- и авиатехники на протяжении нескольких лет, ООО «Стандарт» удалось заслужить доверие клиентов, предлагая:
</p>
 <ul class="red">
	<li>Скупку вторсырья в любых объемах;</li>
	<li>Безопасную переработку комплектующих батарей;</li>
	<li>Бесплатный вывоз лома с объекта (в Москве, Подмосковье);</li>
	<li>Выгодные условия для регулярного сотрудничества;</li>
	<li>Работу с частными и корпоративными клиентами;</li>
	<li>Возможность получать оплату любым удобным способом: наличными, на банковскую карту, электронный кошелек и т.д.</li>
</ul>
 <p class="pt-2">
	 В дальнейшем, закупленные АКБ подвергаются переработке: пластик «растирается» до состояния гранул, используясь при производстве новых батарей, свинец очищается и поставляется на фабрики и заводы по всей России, а электролиты нейтрализуются, после чего их можно безопасно сливать, либо использовать при производстве средств бытовой химии.
