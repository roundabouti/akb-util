                                    <hr class="m-0"/>
                                    <div class="pl-0 pr-1">
                                        <p class="pt-2">При эксплуатации даже нового автомобиля могут возникнуть поломки и нештатные ситуации. Одна из них заключается в том, что кипит аккумулятор при подключении зарядного устройства или во время движения машины. </p>
                                         <p class="pt-0">Причины такого явления могут быть самыми разными. Чаще всего это происходит в результате короткого замыкания, перезаряда АКБ, нарушении правил эксплуатации. </p>
 <p class="pt-0">Быстрые и правильные действия в таком случае помогут избежать серьезных последствий, в число которых входит:</p>

 <p class="pt-0">
                                        <ul class="red">
                                            <li>повреждение кузова и электропроводки в результате протечки электролита;</li>
                                          <li>разрушение свинцовых пластин с последующим взрывом;</li>
                                          <li>повреждение химической коррозией клемм и разъемов;</li>
                                          <li>выход из строя генератора вследствие воздействия на него тока высокой силы.</li>
                                          
                                        </ul>
                                        </p>

<h2 class="text-danger-custom text-center text-xl-left">Должен ли аккумулятор кипеть при зарядке?</h2>
 

 
                                        <p class="pt-0">Большинство современных автомобилей оснащены свинцово-кислотными аккумуляторами, которые состоят из 6 или 8 банок. В каждой сетке находятся свинцовые пластины, погруженные в раствор серной кислоты. Во время реакции химическая энергия трансформируется в электрическую. При подсоединении зарядного устройства на пластины поступает ток, в результате чего выделяется водород и кислород, повышается плотность электролита. Такие процессы считаются кипением, хотя, температура раствора на достигает уровня, при котором он закипает. Такое явление указывает на то, что подзарядка скоро завершится.</p>
 <p class="pt-0">Начинающих автомобилистов интересует, сколько должен кипеть аккумулятор. Как правило, у технически исправного устройства газы начинают выделяться за три часа до того, как оно наберет полную емкость. При этом закипать должны все секции, через 3 часа следует отключить питание.</p>
 <p class="pt-0">Если процесс газообразования затягивается и длится более трех часов, то это может привести к выходу из строя АКБ, выкипанию электролита, замыканию, разрушению свинцовых пластин. </p>

                                        <h2 class="text-danger-custom text-center text-xl-left">Почему аккумулятор начал сразу кипеть при подключении зарядки?</h2>


                                        <p class="pt-2">Мы выяснили, что реакция с образованием пузырьков газа проявляется, когда устройство уже набрало 60% емкости.</p>

                                         
                                       <p class="pt-2">Но, иногда такое явление возникает через 30 минут после подключения к зарядке, что может быть вызвано следующими причинами:</p>


 <p class="pt-0">
                                        <ul class="red">
                                            <li>сульфатированием пластин, в результате чего АКБ теряет емкость;</li>
                                          <li>коротким замыканием в корпусной оболочке.</li>
                                          
                                        </ul>
                                        </p>
 <p class="pt-2">Под действием электротока происходит восстановление свинца, в реакции которого участвуют микрокристаллы. В результате сульфатирования пластинки покрываются налетом и приобретают диэлектрические свойства. Это приводит к повышению плотности раствора и реакции электролиза. АКБ закипает, не набрав нужную емкость. </p>
<h2 class="text-danger-custom text-center text-xl-left">Что делать, если во время зарядки кипят не все банки</h2>
 <p class="pt-2">
	 Секции в аккумуляторной батарее соединяются в последовательном порядке. Общее напряжение получают путем суммирования показателей каждой банки, причем емкость определяется по секции с наименьшим значением. Если реакция происходит не во всех банках, нужно измерить плотность электролита и выявить поврежденный элемент.
</p>
 <p class="pt-2">
	 Измерение плотности проводят следующим образом:
</p>
   <ul class="red">
	<li>&nbsp;аккуратно открутить пробки;</li>
	<li>&nbsp;проверить, чтобы уровень жидкости был не ниже отметки на наружной стороне батареи;</li>
	<li>&nbsp;если электролита мало, нужно долить дистиллированной воды;</li>
	<li>&nbsp;поместить в отверстие ареометр;</li>
	<li>&nbsp;отобрать пробу вакуумной грушей;</li>
	<li>&nbsp;определить фактический показатель плотности по шкале прибора.</li>
</ul>
 <p class="pt-2">
	 Основные причины, по которым не все секции «бурлят»:
</p>
   <ul class="red">
	<li>&nbsp;разрушение пластин в банке;</li>
	<li>&nbsp;попадание внутрь постороннего предмета;</li>
	<li>&nbsp;нормативный срок эксплуатации АКБ подходит к концу, секция разрушилась и в ней находится мутная жидкость.</li>
</ul>
 <p class="pt-2">
	 Если в секцию попал небольшой предмет, его нужно аккуратно извлечь. В таком случае работоспособность можно восстановить на 80 – 90%. После чистки зарядить следует только эту банку.
</p>
 <p class="pt-2">
	 Чаще всего, причиной отсутствия реакции является трещина или другой дефект. Устранение такой неисправности не всегда является целесообразным, поэтому не стоит рисковать и лучше всего установить новую батарею, а старую сдать на утилизацию и получить за это деньги.
</p>
<h2 class="text-danger-custom text-center text-xl-left">Почему кипит АКБ на машине. Что делать в таком случае?</h2>
 <p class="pt-2">
	 Если аккумулятор кипит постоянно, возможно, существуют неполадки в системе электрооборудования. Часто АКБ не может сохранить получаемую электроэнергию и сразу начинает бурлить.
</p>
 <p class="pt-2">
	 Пузыри образуются с нарастающей интенсивностью. Если это происходит сразу после подключения к зарядке, скорее всего, разрушены свинцовые пластины. Эксплуатация такого устройства может быть опасна, а его ремонт – невозможен или нецелесообразен. Поэтому такие батареи подлежат утилизации.
</p>
 <p class="pt-2">
	 Другой причиной неполадок может быть:
</p>
   <ul class="red">
	<li>&nbsp;Перезаряд. Происходит, когда устройство, которое набрало емкость, не отключают вовремя и на него продолжает поступать электроток. Под его воздействием происходит реакция электролиза, испаряется вода, удельный вес которой в составе электролита составляет 65%.</li>
	<li>Перезаряд намного опасней неполного заряда, поскольку может привести к оголению и разрушению свинцовых элементов. Он может быть вызван поломкой генератора или реле, которое отключает электроток.</li>
	<li>&nbsp;Короткое замыкание. Происходит вследствие длительной эксплуатации или правил использования устройства, попадания внутрь посторонних частиц. Основными признаками замыкания является образование пузырьков газа с запаздыванием или отсутствие бурления, снижение показателя напряжения и плотности раствора серной кислоты.&nbsp;&nbsp;</li>
	<li>Некоторые автомобилисты продолжают использовать батарею с секциями, которые замыкают, применяют частую зарядку. Или обращаются к мастерам для ремонта. Это может привести к сульфатации, разрушению пластинок и более серьезным последствиям. Поэтому, лучше всего отправить неисправную батарею на переработку.</li>
	<li>&nbsp;Чрезмерная нагрузка на электрическую систему. Если использовать одновременно все электроприборы – включить фары, акустическую систему, климат-контроль, то может произойти перегрузка электрооборудования. Характеристик генератора может быть недостаточно, чтобы восполнить заряд, что приводит к чрезмерному нагреву электролита.</li>
	<li>&nbsp;Нарушение правил эксплуатации. Негативно сказывается на работе АКБ частый запуск и выключение двигателя, игнорирование признаков неполадок, отсутствие контроля за уровнем жидкости и ее плотностью.</li>
</ul>
 <p class="pt-2">
	 Если проявляются признаки производственного брака или АКБ получила видимые повреждения, ее нормальный режим функционирования нарушен и сопровождается нетипичными явлениями, опытные автомастера рекомендуют сдать ее на утилизацию и установить новое устройство. Это позволит избежать непредвиденных ситуаций в пути, серьезных поломок и даже взрыва.
