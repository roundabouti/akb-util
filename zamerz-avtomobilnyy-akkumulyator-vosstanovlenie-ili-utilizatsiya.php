</p>
<h2 class="text-danger-custom text-center text-xl-left">Почему и при какой температуре замерзает аккумулятор в машине</h2>
<p class="pt-2">
	 Чтобы понять причину замерзания, достаточно разобраться, как устроена «классическая» свинцово-кислотная батарея, служащая источником питания для большинства современных авто.&nbsp;<br>
</p>
<p class="pt-2">
	 АКБ состоит из:
</p>
<ul class="red">
	<li>Корпуса, выполненного из материалов, устойчивых к агрессивным химическим веществам, высоким температурам, механическим повреждениям;</li>
	<li>Свинцовых пластин, способных поглощать, хранить и отдавать электрическую энергию при необходимости;</li>
	<li>Электролита – специальной токопроводящей жидкости, «приготовленной» из двух ингредиентов: дистиллированной воды и серной кислоты.</li>
</ul>
<p class="pt-2">
	 Как это работает? Пластины погружают в корпус (именуемый банкой), заливают электролитом, подключают контакты. Ёмкость батареи зависит от того, количества свинца и объема жидкости. При этом в процессе эксплуатации она имеет свойство нагреваться и испаряться, из-за чего ресурс его работы ограничен (в идеале 3-5 годами).
</p>
<p class="pt-2">
	 “Слабым звеном” этой конструкции является электролит, состоящий из воды на 65%. Соответственно, оставшиеся 35% - это кислота, устойчивая к низким температурам. При грамотном соблюдении пропорции, смесь сможет выдерживать около -35-40°С, чего достаточно для большинства регионов (в т.ч. и России). Но в процессе эксплуатации, кислота будет “выгорать”, оседая солью на пластинах, в то время как количество H2O будет расти. Зарядка позволяет повысить плотность электролита, уберегая жидкость от замерзания. Следовательно, в отсутствие подпитки, вода вытесняет кислоту, повышая вероятность замерзания жидкости.
</p>
<p class="pt-2">
	 При этом часто, быстрая потеря заряда на морозе – следствие утечки тока в цепи. Случается это из-за неправильного подключения штатных приборов – магнитолы, сигнализации, центральных замков, видеорегистратора и т.д. Постоянная работа устройств разрядит АКБ менее, чем за сутки. А если на дворе стоят «зверские» холода – «убьет» его окончательно, без шанса на восстановление нормальной работоспособности. Особенно, если машину пришлось оставить на 3-4 дня. Поэтому прежде, чем поставить машину на «зимовку» стоит проверить, не происходит ли перерасход энергии в результате несоблюдения требований к подключению «дополнительной» аппаратуры.
</p>
<h2 class="text-danger-custom text-center text-xl-left">Признаки замерзшего аккумулятора</h2>
<p class="pt-2">
	 Предположим, вы не смогли найти место для стоянки автомобиля и вам пришлось оставить его под открытым небом на несколько дней. И за это время, погода успела испортиться: температура воздуха перевалила за отметку в -30, а метель «замела» вашего железного товарища по лобовое стекло. Отчистив машину от снега, вы, как обычно, достаете ключи и пробуете снять его с сигнализации. Но в ответ на эту попытку не происходит ничего. Абсолютно никакой реакции. Открыв двери ключом, привычным движением вставляете его в замок зажигания и обнаруживаете, что автомобиль не подает никаких признаков жизни.
</p>
<p class="pt-2">
	 Дочитав до этого момента, мы понимаем, что вероятнее всего, причина проблемы – замерзший аккумулятор.&nbsp;<br>
</p>
<p class="pt-2">
	 Открыв крышку капота, вы можете увидеть:
</p>
<ul class="red">
	<li>Что корпус АКБ покрылся плотным слоем инея (лучший исход);</li>
	<li>«Трансформацию» батареи из квадратной в круглую (без повреждений);</li>
	<li>Физические повреждения, трещины (все, пиши пропало).</li>
</ul>
<p class="pt-2">
	 Как действовать? Давайте разбираться!<br>
</p>
<h2 class="text-danger-custom text-center text-xl-left">Что делать, если замерз аккумулятор</h2>
<p class="pt-2">
	 Предпринимать какие-либо действия нужно, исходя из сложившейся ситуации. В теории, «обледеневшую» батарею можно реанимировать (даже в домашних условиях).&nbsp;<br>
</p>
<p class="pt-2">
	 Для этого:
</p>
<ul class="red">
	<li>Проводим визуальное обследование «пациента» на предмет вздутия, прорыва или трещин, из которых выступает лед или видны протечки жидкости;</li>
	<li>Снимаем АКБ, относим в теплое сухое помещение, ставим её в емкость соответствующего размера и ждем, пока она оттает (чтобы убедиться в отсутствие микротрещин);</li>
	<li>После «прогрева», подключаем зарядку с малыми токами, на длительное время: если все в норме, он «оживет» через 8-10 часов.</li>
</ul>
<p class="pt-2">
	 На деле же, «оживлять» их нецелесообразно. Ведь в 8 из 10 случаев, из-за сильных морозов они покрываются трещинами настолько, что ремонт или восстановление оказываются нецелесообразным вложением – траты на проведение реанимационных мероприятий будут гораздо выше, чем стоимость новой батареи.
</p>
<p class="pt-2">
	 При этом стоит отметить, что при замерзании, вода может вступить в реакцию со свинцом и попросту “разорвать” пластины. В таком случае, слитый электролит будет иметь неприятный оттенок и запах. Конечно же, некоторые умельцы умудряются исправить и это, но езда с черным или зеленоватым электролитом – прямая угроза для безопасности водителя и пассажиров авто.
</p>
<p class="pt-2">
	 Поэтому эксперты рекомендуют утилизировать АКБ, попавшие под действие низких температур. Ведь:
</p>
<ul class="red">
	<li>Неисправность может проявиться уже через несколько дней активной эксплуатации машины;</li>
	<li>После промерзания автомобиль не будет заводиться «с полоборота», а просьбы «прикурить» или «подтолкнуть» станут для вас привычным явлением;</li>
	<li>Любите слушать музыку в поездках? Забудьте об этом: с неисправным аккумулятором единственные звуки, которые можно услышать – молитвы в вашей голове.</li>
</ul>
