                                            <li>Восстанавливая «чистую» серную кислоту для производства новых свинцово-кислотных аккумуляторных батарей.</li>
                                        </ul>
                                        </p>
                                        <p class="pt-0">При этом стоит отметить, что даже после нейтрализации, сливать отработанную жидкость нужно, соблюдая технику безопасности, в специально оборудованных помещениях.</p>

                                        <h2 class="text-danger-custom text-center text-xl-left">Куда и почему нельзя выливать электролит</h2>
                                        <p class="pt-0">Отработанные электролиты категорически запрещено сливать в: </p>
                                        
