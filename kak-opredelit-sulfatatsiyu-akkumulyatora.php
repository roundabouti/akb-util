                                        <h1 class="text-danger-custom text-center text-xl-left">Как определить сульфатацию аккумулятора</h1>
                                    </div>
                                    <hr class="m-0"/>
                                    <div class="pl-0 pr-1">
           
<p class="pt-2">
	 Это образование небольших частичек из кислоты и элементов свинца на пластинах. Постепенно эти частички покрывают всю поверхность пластин. Сульфат препятствует нормальной зарядке и отдаче энергии, емкость АКБ снижается. Первые признаки этого можно обнаружить, если внимательно осмотреть корпус снаружи. Если есть налет на клеммах – то значит, что сульфатация аккумулятора началась.
</p>
<h2 class="text-danger-custom text-center text-xl-left">Что такое сульфатация пластин АКБ</h2>
<p class="pt-2">
	 Под действием серной кислоты пластины покрываются тонкой пленочкой сульфата. В нормальном состоянии она состоит из небольших кристаллов, которые в процессе зарядки уходят обратно в жидкость, очищаясь с пластины. Но если нормальные условия эксплуатации батареи не соблюдаются, (например – если она разряжена и ее долго не заряжают), то начинается процесс образования крупных кристаллов, которые невозможно растворить при заряде. Такие кристаллы начинают накапливаться на поверхностях пластин, что приводит к снижению емкости. Корректная работа источника энергии нарушается. Чем больше разряд, тем крупнее становятся частички сульфата. Если АКБ довести до состояния полного разряда, то образовавшийся налет не получится растворить даже при зарядке. Из-за этого аккумулятор может полностью потерять емкость, т.е. совсем выйдет из строя.
</p>
<h2 class="text-danger-custom text-center text-xl-left">Причины и признаки сульфатации</h2>
<p class="pt-2">
	 Проявляется это в следующих признаках:
</p>
<ul class="red">
	<li>резко снизилась емкость;</li>
	<li>температура жидкости внутри резко повышается при запуске двигателя;</li>
	<li>цвет внутри изменился;</li>
	<li>стало выделяться много газов из банок.</li>
</ul>
<p class="pt-2">
	 Многие водители не задумываются о том, какие процессы происходят внутри АКБ. Банки закрыты пробками, визуально обнаружить образование налета невозможно. Есть ряд признаков, которые заметны снаружи, и по которым можно определить, что сульфатация в батарее идет полным ходом.
</p>
<p class="pt-2">
	 Самый простой способ – это открутить пробки (если АКБ относится к категории обслуживаемых). При помощи фонарика можно увидеть, в каком состоянии находятся пластины. Сульфат на них представляет собой белый или светло-коричневый налет.
</p>
<p class="pt-2">
	 Если во время зарядки электролит слишком быстро начинает кипеть и слишком быстро заряжается – то это явный признак образования налета на пластинах.&nbsp; Если зашло слишком далеко, то закипеть может уже минут через 30 после начала зарядки.
</p>
<p class="pt-2">
	 Появление белого налета на поверхности батареи (в том числе на клеммах) может считаться нормальным, если происходит не очень часто. Хотя бы иногда даже абсолютно исправный аккумулятор начинает кипеть. Но если такое происходит слишком часто, то это говорит о нарушении нормальных рабочих процессов в батарее.
</p>
<p class="pt-2">
	 Если налет сульфата стал слишком крупным, емкости АКБ может не хватить для запуска мотора, он будет плохо запускаться при полностью заряженной батарее. Если такое происходит, то для проверки можно провести маленький эксперимент: двигатель заглушить, включить фары. Минут через 15 они погаснут, а аккумулятор окажется полностью разряженным.
</p>
<p class="pt-2">
	 Признаков начала сульфатации достаточно, но многие водители не обращают на них внимания до тех пор, пока не возникнут проблемы в дороге.
</p>
<p class="pt-2">
	 Если напряжение и сила тока при полной зарядке не соответствуют значениям, то это один из явных признаков того, что батарее требуется профилактика.
</p>
<p class="pt-2">
 <b>Причины сульфатации</b>
</p>
<ol>
	<li>Глубокий разряд. Если такая ситуация произошла 2-3 раза, то аккумулятор наверняка выйдет из строя.</li>
	<li>Низкие (морозные) температуры. Сам по себе мороз не провоцирует напрямую процессы сульфатации, но является одной из косвенных причин этого. В холодную погоду для запуска стартера требуется больше энергии, чем летом, поэтому при повороте ключа существенно вырастает нагрузка на АКБ. В движении при сильном морозе хуже проходит зарядка. Особенно это актуально для поездок на небольшие расстояния. Если водитель в мороз запускает двигатель, а минут через 15 глушит его – то этим самым создает очень некомфортные условия работы АКБ.</li>
	<li>Высокая температура точно так же вредна, как и низкая. При езде в условиях сильной летней жары батарея перегревается. В таких условиях все химические и физические процессы протекают намного быстрее, в том числе и сульфатация. Поэтому летом рекомендуется внимательно следить за уровнем заряда, чтобы снизить активность процесса образования частичек сульфата.</li>
	<li>Использование электролита или серной кислоты высокой концентрации способствует образованию частичек сульфата. Некоторые неопытные водители ошибочно думают, что сульфатный налет на пластинах можно удалить при помощи концентрированной кислоты.&nbsp; Этого делать ни в коем случае нельзя. Кристаллы не только не растворятся, а станут еще крупнее.</li>
</ol>
<p class="pt-2">
	 Необходимо помнить, что все процессы в аккумуляторе не останавливаются, даже если нет подключения потребителей. При хранении в разряженном состоянии на протяжении нескольких месяцев батарея может потерять емкость, что приведет к активному образованию налета на пластинах. А при отсутствии напряжения кристаллы не будут растворяться, что приведет к полному выходу аккумулятора из строя.
</p>
<p class="pt-2">
 <b>Как устранить сульфатацию аккумулятора?</b>
</p>
<p class="pt-2">
	 Проблема в том, что постепенно образуются слишком большие частички сульфата, которые не растворяются в электролите. В результате возникает ситуация, когда кислота перестает принимать сульфат по установленной схеме. Зарядить полностью и поднять плотность до нужного значения в таком состоянии невозможно.
</p>
<p class="pt-2">
	 Чтобы все кристаллы растворились, плотность электролита должна быть понижена. Для этого нужно:
</p>
<ol>
	<li>электролит полностью заменить дистиллированной водой;</li>
	<li>подключить зарядное устройство;</li>
	<li>заряжать до тех пор, пока плотность не достигнет максимального значения.</li>
</ol>
<p class="pt-2">
	 Эти действия нужно выполнить несколько раз, после чего залить готовый электролит.
</p>
<p class="pt-2">
	 Остановить сульфатацию можно только на самой первой стадии. Для этого нужно:
</p>
<ul class="red">
	<li>протереть батарею;</li>
	<li>долить электролит;</li>
	<li>подключить ЗУ.</li>
</ul>
<p class="pt-2">
	 Сила тока при этом должна быть установлена на 8-часовой режим. Когда нагреется до 43 градусов, силу тока нужно уменьшить. Когда зарядка достигнет нужного номинального значения, процесс нужно продолжать, пока плотность электролита не будет постоянной в течение 4 часов. После этого АКБ нужно разрядить в течение 6 часов и снова зарядить.
</p>
<h2 class="text-danger-custom text-center text-xl-left">Последствия для батареи и автомобиля</h2>
<p class="pt-2">
	 Если вовремя не принять нужных мер, то такое состояние постепенно приведет к полному выходу АКБ из строя. Восстановление ее будет невозможно, придется сдавать на утилизацию. Не стоит пытаться избавиться от нерабочего кальциевого или свинцового аккумулятора, просто выбросив его на свалку – от этого могут быть неприятные последствия. Сдавать нерабочие аккумуляторы разного типа можно только в специальные пункты приема. В Москве такие услуги оказывает компания «Стандарт».
</p>				

