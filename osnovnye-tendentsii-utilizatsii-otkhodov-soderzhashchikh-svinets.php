                                    <div class="pl-0 pr-1">
                                        <p class="pt-2"> Сейчас главной сферой применения свинца являются химические источники тока и ведущее место среди них занимают аккумуляторные свинцовые батареи.</p>
    <p class="pt-2">
     Около 70% производимого свинца приходится на выпуск кислотных аккумуляторов, которым нет альтернативы в транспорте и промышленности.
</p>
    <p class="pt-2">
 В сфере сбора и переработки аккумуляторного лома назрела необходимость внесения изменений, связанных с экологической безопасностью. Потенциальная химическая опасность заключается в том, что около 60% массы стандартного аккумулятора составляет свинецсодержащий материал, а в качестве электролита используется серная кислота.</p>
    <p class="pt-2">
 Переработка отработанных аккумуляторных батарей различного типа позволяет организовать рециклинг вторичного свинца. В экономически развитых странах - Японии, США, Южной Кореи - мера переработки аккумуляторного лома достигает 95 ... 98%.</p>
    <p class="pt-2">
 Для этих стран на законодательном уровне приняты акты и постановления по утилизации отработанных аккумуляторных батарей. Как правило, вторичный свинец, полученный после переработки лома, направляют дальше на производство новых аккумуляторов, что позволяет осуществлять рециклинг не только свинца, но и ряда других металлов в составе сплава - трубы, меди, олова и мышьяка. К методам рафинирования свинца относятся удаление меди из чернового свинца вместе с пиритом, щелочное рафинирование с помощью процесса Харриса, окислительное удаление труб и др.</p>
    <p class="pt-2">
 Как показывают результаты исследований отечественных и зарубежных аналитиков, количество аккумуляторного лома в ближайшие годы будет расти в связи с развитием автомобильной отрасли во всем мире. Однако, ведутся разработки современных технологий по извлечению свинца с других отходов металлургической отрасли, в том числе - с пылегазовых свинецсодержащих образований, которые в большом количестве поступают в атмосферу.</p>
    <p class="pt-2">
 Главную массу вторичного свинца получают как результат переработки лома в шахтных, отражательных, электрических и роторных короткобарабанных печах. В мировой практике широко применяют плавление вторичного свинцового сырья в барабанных печах, длина которых близка к диаметру, а скорость вращения составляет 0,5 ... 2,5 об/мин. Главными достоинствами печей этого типа являются:</p>
 
<ul class="red">
    <li>переработка шихты переменного состава и получения, в зависимости от ее состава, чернового сплава для производства «мягкого» свинца или аккумуляторных сплавов с заданным содержанием легирующих добавок;</li>
    <li>возможность полного выпуска расплава из печи, что позволяет избежать инерции в управлении технологическими процессами и своевременно выявить изменения в состоянии футировки;</li>
    <li>оптимизация работы пылегазового тракта и аспирационных систем;</li>
    <li>высокая интенсивность и простота технологических процессов, удобство обслуживания.</li>
</ul>
    <p class="pt-2">
 Основным недостатком короткобарабанных печей является невозможность получения водонерастворимых фаялитовых шлаков, которые не требуют значительных затрат на утилизацию. Известно, что во время плавки в печах этого типа используют соду и получают расплав, который плохо расслаивается. Экологически безопасным является шлак с высоким содержанием кремнезема, который отвечает экологическим нормам.</p>
    <p class="pt-2">
 Перспективным методом переработки свинецсодержащего лома является извлечение свинца в порошковом виде из отработанных аккумуляторов.</p>
    <p class="pt-2">
 Процессы переработки свинцового лома методами электрометаллургии связанны с образованием значительного количества шлака и хлоридного пыли.</p>
    <p class="pt-2">
 Аккумуляторный лом может быть также переработан методами гидрометаллургии.</p>
    <p class="pt-2">
 Одним из экологически чистых методов утилизации свинца является растворение свинцовых пластин и электролитическое получение свинца. Способ может быть применен и для отработанных аккумуляторов. Процесс выделения свинца получают при силе тока 5...15 мА/см2. Электролитом служит малеиновая кислота. Как результат достигают полной утилизации свинца из отработанных аккумуляторных батарей.</p>
    <p class="pt-2">
 Кроме непосредственной переработки аккумуляторного лома, важным моментом является рафинирование полученного чернового свинца. Если в извлечении свинца с отработанных аккумуляторных батарей и других свинцовых полупродуктов имеют место значительные успехи, что положительно влияет на состояние экологии, то сопутствующие процессу элементы - сера и хлор - практически не извлекают или извлекают в недостаточной степени.</p>
    <p class="pt-2">
 Главной сложностью переработки пасты, содержащей более 50% сульфата свинца, является обеспечение вывода серы в продукты, удобные для захоронения. Трудности очищения газов от SO2 привели к разработке технологий с использованием флюсов на основе кальцинированной соды, которая связывает серу в нелетучее соединение Na2S.</p>
    <p class="pt-2">
 В сфере рафинирования чернового свинца целесообразно применение мягких стандартов и норм по содержанию в свинце после обработки таких примесей, как серебро и висмут (через их тяжелое добычи) по примеру «мягкого» свинца с содержанием сурьмы до 0,005%. В пользу этого свидетельствует и регламентация содержания примесей в свинцовом порошке, который служит сырьем для обработки пластин аккумуляторов. </p>
