                                        <p class="pt-2">
    <p class="pt-2">
     Возможно ли восстановление гелевого аккумулятора, если он вышел из строя? Чаще всего нет. Это стоит пытаться только в тех случаях, когда просто высох гель, но все остальное осталось целым и невредимым. Но если есть даже небольшие повреждения пластин хотя бы в одной из банок – то проводить «реанимацию» не стоит и пытаться.
</p>
<p class="pt-2">
     Такой тип источников электроэнергии в последние годы получил широкое распространение в бытовой технике. Обычно их используют в ИБП разного типа и предназначения, в разных портативных устройствах. Один агрегат может прослужить несколько лет, после чего чаще всего начинается снижение емкости.
<p class="pt-2">
<h2 class="text-danger-custom text-center text-xl-left">Подлежит ли гелевый аккумулятор восстановлению</h2>
<p class="pt-2">
     В большинстве случаев гелевые АКБ, находящиеся «на грани смерти», оживать невозможно. Это отнимет только время и силы. Проще купить новую батарею, а старую сдать на утилизацию.
</p>
<p class="pt-2">
 <b>Основные признаки и причины, по которым не стоит пытаться восстановить батарею</b>
</p>
<ol>
    <li>Вздутие. Если одна или несколько банок приняли округлую форму, то значит – свинцовые пластины стали отслаиваться. Гель и электролит потеряли нормальное химическое взаимодействие с пластинами. Такие признаки видны невооруженным взглядом, и безо всяких приборов можно «констатировать смерть» АКБ.</li>
    <li>Механическое разрушение пластин. Такое происходит обычно тогда, когда ИБП очень долго стоит и практически непрерывно эксплуатируется в таком месте, где нет нормальной вентиляции, проветривания. Из-за постоянного воздействия высокой температуры содержимое банок постепенно теряет свою прочность. Стекловолокно, пластины и жидкость внутри начинают рассыпаться и расслаиваться. В самых тяжелых случаях это приводит к тому, что внутри остается только мусор. Самый простой способ проверить гелевый аккумулятор на целостность его «внутренностей» - это немного потрясти. Если внутри что-то болтается – то батарея вышла из строя. Также можно открутить пробки и посветить внутрь фонариком. Если пластины не имеют четко различимого контура – то они стали разрушаться.</li>
</ol>
<p class="pt-2">
     Если гелевый слой высох, но пластины целые – то можно попытаться восстановить аккумулятор.
</p>
<h2 class="text-danger-custom text-center text-xl-left">Стоит ли пытаться самостоятельно восстановить емкость гелевого АКБ</h2>
<p class="pt-2">
     Если визуально установить дефекты и повреждения не удалось, то необходимо проверить аккумулятор своими руками. Для этого используются специальные приборы, например – вольтметр с нагрузочной вилкой.
</p>
<h4 class="text-danger-custom text-center text-xl-left">Порядок действий:
</h4>
<ol>
    <li>установить электронапряжение в нужном диапазоне;</li>
    <li>дать нагрузку;</li>
    <li>в течение нескольких секунд имитировать работу электродвигателя;</li>
    <li>проконтролировать скорость восстановления напряжения.</li>
</ol>
<p class="pt-2">
     Если параметры электротока падают быстро, то значит батарея приближается к выходу из строя. Явным признаком «старости» аккумулятора является быстрая зарядка. Новая гелевая АКБ заряжается долго.
</p>
<p class="pt-2">
     Гелевые АКБ относятся к категории необслуживаемых, т.е. они практически не подлежат восстановлению. Но в некоторых случаях можно пробовать их оживить. Для этого понадобятся несколько простых инструментов и приспособлений.
</p>
<p class="pt-2">
     Производители аккумуляторов уверяют, что уровень геля в батарее не меняется на протяжении многих лет. На самом деле он постепенно высыхает. Обычно это происходит по причине того, что регулярно подается слишком сильный электроток при зарядке.
</p>
<p class="pt-2">
     Чтобы восстановить уровень жидкости внутри, надо демонтировать крышку и пробки. Есть такие модели гелевых АКБ, в которых каждая банка имеет свою крышку. Приходится снимать все крышки и откручивать пробки.
</p>
<p class="pt-2">
     После этого в каждую банку при помощи шприца надо долить по 2 «кубика» дистиллированной воды. Слишком много жидкости заливать нельзя. Вода нужна для того, чтобы гель вернулся в нормальное состояние.
</p>
<p class="pt-2">
     Впитывание воды занимает около 40 минут. По истечении этого времени нужно проверить уровень раствора внутри батареи, и либо добавить еще воды, либо удалить излишки. Пробки и крышки вернуть на место. Чтобы зафиксировать их, можно использовать клей.
</p>
<p class="pt-2">
     Следующим этапом является зарядка АКБ. Ее можно начинать через несколько часов после заливки воды. Перед началом зарядки на крышку батареи надо поставить тяжелый предмет, чтобы крышка не слетела под давлением, т.к. при зарядке будет выделяться водород.
</p>
<p class="pt-2">
     Сначала подается напряжение в 14-15 В. Зарядку требуется проводить до тех пор, пока АКБ не начнет потреблять ток. Это занимает обычно 12-14 часов. Если за это время аккумулятор не зарядился, то надо увеличить напряжение до 20 В. После этого оставлять его без внимания нельзя. Как только ток пойдет, надо будет постепенно снижать напряжение до 14 В. Иначе может начаться задымление или даже пожар.
</p>
<p class="pt-2">
     Если емкость сильно упала, то можно попробовать провести циклическую зарядку, чтобы оживить батарею. В этом случае надо сначала полностью зарядить АКБ, после чего снова разрядить. Это можно сделать, например, при помощи лампочки небольшой мощности. Данную процедуру можно повторить 3-5 раз. При этом надо следить, чтобы при зарядке напряжение не падало ниже 10,5 В. Если и после такой операции емкость батареи не восстановилась – то аккумулятор совсем испорчен.
</p>
<p class="pt-2">
     В большинстве случаев пытаться самостоятельно восстановить вышедший из строя гелевый аккумулятор не рекомендуется. Если есть явные повреждения на корпусе – то такую батарею надо сдавать на утилизацию. Выкидывать на свалку или оставлять в гараже нельзя – это опасно и для окружающей среды, и для здоровья человека. Сдавать можно только в специальные пункты приема. В Москве такие услуги оказывает ООО «Стандарт».
</p>        

