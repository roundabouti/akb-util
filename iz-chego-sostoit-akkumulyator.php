<h2 class="text-danger-custom text-center text-xl-left">Схема устройства аккумулятора автомобиля</h2>
<p class="pt-2">
	 Основные составные части аккумуляторной батареи:
</p>
<ul class="red">
	<li>корпус;</li>
	<li>крышка;</li>
	<li>плюсовой и минусовой наконечники, к которым подсоединяются клеммы;</li>
	<li>заливные колодцы;</li>
	<li>соединительные элементы;</li>
	<li>решетки;</li>
	<li>электролит.</li>
</ul>
<p class="pt-2">
	 Основными рабочими элементами аккумуляторной батареи являются электроды. Между ними находятся пластины, изготавливающиеся из материалов, не вступающих в химическую реакцию с кислотами. Основное предназначение пластин – препятствовать замыканию электродов с разными зарядами, погруженных в электролит.
</p>
<p class="pt-2">
	 Электролит – это раствор серной кислоты и дистиллированной воды. В него погружены электроды и сепараторы. От концентрации кислоты зависит электропроводящая способность жидкости. Чем большее значение имеет этот параметр, тем меньше сопротивление батареи, соответственно – ниже потери энергии. Доля кислоты в разных типах АКБ может быть выше или ниже – все зависит от климатических особенностей. Если техника эксплуатируется в условиях сильных морозов, то недостаток кислоты может привести к замерзанию электролита.
</p>
<p class="pt-2">
	 Электрод – это элемент (токоотвод) в виде тонкой свинцовой решетки, в каждой ячейке которой находятся небольшие вставки.
</p>
<p class="pt-2">
	 Они изготавливаются из:
</p>
<ul class="red">
	<li>спрессованного порошкообразного диоксида свинца (для анодных частей);</li>
	<li>металлического свинца (для катодных).</li>
</ul>
<p class="pt-2">
	 Порошкообразные вещества используются для увеличения поверхности раздела между электропроводящей жидкостью и твердыми элементами, чтобы энергоемкость батареи была максимальной.
</p>
<p class="pt-2">
	 В состав металла входят некоторые компоненты, которые формируют его литейные и антикоррозионные свойства. Технические характеристики аккумулятора во многом зависят от состава сплава и формы электродов.
</p>
<p class="pt-2">
	 Чтобы электроды с разными полюсами (положительные и отрицательные) не контактировали друг с другом (соответственно – чтобы не было короткого замыкания), в аккумуляторах используют специальные разделительные элементы – сепараторы.
</p>
<h2 class="text-danger-custom text-center text-xl-left">Схема зарядного устройства для автомобильного аккумулятора</h2>
<p class="pt-2">
	 Используется однофазная сеть в 220 вольт. Прибор преобразует это напряжение, которое подается на клеммы аккумулятора. Существуют разные схемы преобразования промышленного напряжения:
</p>
<ul class="red">
	<li>с применением электромеханических трансформаторов, в основе работы которых лежит принцип электромагнитной индукции (ЭИ);</li>
	<li>с использованием электронных трансформаторов;</li>
	<li>без трансформаторов.</li>
</ul>
<p class="pt-2">
	 Принцип ЭИ позволяет обеспечить отделение потенциалов питающей цепи от потребителя, чтобы избежать повреждения АКБ в случае каких-либо неисправностей.
</p>					

