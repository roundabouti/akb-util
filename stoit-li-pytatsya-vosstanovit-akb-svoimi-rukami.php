                  <p class="pt-2">
	 Аккумулятор, разрядившийся в пути – это обидно. А если он выходит из строя неожиданно – это обиднее вдвойне. А учитывая, что новый стоит отнюдь недешево, большинство водителей предпринимают попытки реанимировать имеющуюся батарею «подручными» средствами.&nbsp;
</p>
<p class="pt-2">
	 Поэтому мы решили рассказать, что приводит к такой поломке, как восстановить АКБ и стоит ли заниматься этим вообще.
</p>
<h2  class="text-danger-custom text-center text-xl-left">Как восстановить старый автомобильный аккумулятор</h2>
<p class="pt-2">
	 Прежде чем приступить к «оживлению» необходимо разобраться, почему же устройство вышло из строя.
</p>
<p class="pt-2">
	 Причин этому может быть несколько:
</p>
<ul class="red">
	<li>Саморазряд. Одна из самых распространенных проблем, свойственных абсолютно всем источникам питания, использующимся не только в транспортных средствах. При этом она касается даже новых, неиспользованных АКБ, стоящих на витрине ближайшего магазина: за сутки простоя, они могут разряжаться на 1%. В случае со старыми источниками питания, в период простоя они могут терять до 3% заряда. Ведь при простое, электролит расслаивается, оседая на дне банки в виде плотной, вязкой жидкости, собирающей «паразитные» токи, приводящие к самопроизвольной разрядке.</li>
	<li>Окисление выводных штырей. Вторая по «популярности», а также самая простая неисправность, случающаяся из-за попадания электролита или его паров на контакты между штырями и клеммами. В большинстве случаев, всему виной – постоянная тряска, приводящая к утечке вещества, реже – перегрузки, провоцирующие его испарение;</li>
	<li>Короткие замыкания. Поломка, являющаяся следствием предыдущих проблем. Возникает в результате излишней тряски плохо закрепленной АКБ, неправильно подобранного электролита, повреждения пластин из-за подачи слишком высокого тока, либо несоблюдения времени заряда;</li>
	<li>Физические повреждения корпуса. Сколы, трещины, деформации в результате падения с высоты, плавление отдельных элементов, износ контактов и т.д. – все это приводит к неисправности и невозможности безопасного использования;</li>
	<li>Сульфатация внутренних пластин. Из-за продолжительного простоя, ошибки при выборе токопроводящей жидкости, снижения уровня электролита в баке, а также продолжительной эксплуатации транспортного средства при полной разрядке источника питания, на его пластинах могут образоваться кристаллы сернокислого свинца. При большом скоплении, они приводят к снижению емкости АКБ и его преждевременному разряду;</li>
	<li>Обрыв внутренней сети. Поломка, характерная для старых батарей, происходит вследствие отсоединения пластин от выводящих клемм.</li>
</ul>
<p class="pt-2">
	 Простейшая диагностика поможет выявить причину неисправности, а также оценить целесообразность восстановления. В первом случае, достаточно «подзаряжать» АКБ в моменты простоя хотя бы 1-2 раза в месяц путем использования малых токов. При окислении, выводные штыри можно «почистить» с помощью мелкозернистой наждачной бумаги, обработать специальным техническим вазелином.
</p>
<p class="pt-2">
	 Ремонтировать аккумулятор, пострадавший из-за короткого замыкания, можно лишь в случае, если он является обслуживаемым: в остальном, батарея подлежит утилизации. Для восстановления корпуса после физических повреждений нужно оценить степень деформации, при проведении работ электролит сливается, а сам бак просушивается и обрабатывается специальными растворами, устраняющими остатки токопроводящей жидкости.
</p>
<p class="pt-2">
	 Десульфатация пластин проводится в несколько этапов:
</p>
<ul class="red">
	<li>Слитие «отработанного» электролита;</li>
	<li>Подготовка новой токопроводящей жидкости с десульфатирующей присадкой;</li>
	<li>Заправка батареи получившимся веществом;</li>
	<li>Подключить зарядное устройство с небольшим током;</li>
	<li>Проследить за напряжением на клеммах, уменьшая ток при необходимости.</li>
	<li>При этом стоит отметить, что в идеале, после чистки от кристаллов сернокислого свинца, батарею следует полностью разрядить и зарядить по новой: таким образом, вы сможете восстановить изначальную емкость АКБ.</li>
</ul>
<h2  class="text-danger-custom text-center text-xl-left">Можно ли восстановить аккумулятор автомобиля в домашних условиях?</h2>
<p class="pt-2">
	 К сожалению, «потратиться» на новую АКБ могут далеко не все. Но самостоятельное восстановление батареи – лишь временное решение. Ведь если проблема дала о себе знать хотя бы раз, в будущем она непременно проявится снова.
</p>
<p class="pt-2">
	 Для быстрой реанимации износившегося источника питания «подручными» средствами достаточно:
</p>
<ul class="red">
	<li>Зарядить «до предела»;</li>
	<li>Слить токопроводящую жидкость;</li>
	<li>Промыть АКБ дистиллированной водой (лучше – несколько раз);</li>
	<li>Приобрести жидкость для очистки, залить её в бак, оставить на 40-50 минут;</li>
	<li>Слить «промывку», провести дополнительную очистку корпуса;</li>
	<li>Залить новый электролит (с рекомендуемой плотностью);</li>
	<li>Подключить батарею к зарядке, затем разрядить её полностью (для восстановления ёмкости).</li>
</ul>
<p class="pt-2">
	 Но ремонтировать источник питания нужно далеко не всегда. Ведь во-первых, большинство современных аккумуляторов являются необслуживаемыми, следовательно. после поломки или отработки своего ресурса, подлежат немедленной утилизации.
</p>
<p class="pt-2">
	 Во-вторых, старые батареи стоит восстанавливать лишь в случае, если их составляющие элементы не требуют замены, т.к. в этом случае вы не сможете найти новые детали, а «старые» запчасти могут выйти из строя в самый неожиданный момент. Поэтому задумайтесь, а не целесообразнее ли будет сдать старый, износившийся АКБ в утиль и поставить на его место новый?
</p>
<p class="pt-2">
	 Поэтому если в вашем гараже скопилось несколько десятков «ненужных» АКБ, задумайтесь об их утилизации. А ООО «Стандарт» готова заплатит за них неплохую сумму, а также даст ему «вторую жизнь» с помощью переработки: поврежденный пластиковый корпус будет перемолот до состояния гранул и послужит материалом для новых изделий, переплавленный и очищенный свинец найдет применение в производстве все тех же аккумуляторов, а отработанная и непригодная к использованию токопроводящая жидкость будет использована в качестве основы для различных средств бытовой химии: моющих веществ, автомобильной косметики и т.д.
</p>					

