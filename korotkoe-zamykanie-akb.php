                                        <p class="pt-2">В любом автомобиле возможно короткое замыкание аккумулятора, которое может привести к неприятным последствиям. Чтобы его избежать, рекомендуется знать основные причины и признаки. </p>
<h2 class="text-danger-custom text-center text-xl-left">Ток короткого замыкания автомобильного аккумулятора</h2>
<p class="pt-2">
     В нормальном состоянии пластины с положительным и отрицательным зарядом в аккумуляторе изолированы друг от друга.</b>
</p>
  

<p>
     Если по каким-то причинам между ними происходит соединение, обеспечивающее течение электротока – происходит короткое замыкание (КЗ).&nbsp;
</p>
<p>
     Это очень опасное техническое явление, которое не только разрушает сам аккумулятор, но и может навредить здоровью человека. Батарея после этого не подлежит ремонту и восстановлению.
</p>
<h2 class="text-danger-custom text-center text-xl-left">Причины и признаки короткого замыкания</h2>
<p>
     Каждому автовладельцу рекомендуется хорошо знать все основные признаки КЗ аккумулятора, чтобы уметь вовремя распознать приближение этой ситуации и успеть снять батарею до того, как произойдет самое неприятное.
</p>
<p>
     <b>Причины:</b>
</p>
<ul class="red">
    <li>искривление металлических пластин, происходящее из-за нарушения правил эксплуатации или естественного износа материалов;</li>
    <li>окислительные процессы, вследствие которых частицы свинца смещаются с пластин, накапливаются между ними, образуя соединяющий мостик-проводник;</li>
    <li>шлам (мусор, частички материалов), накапливающийся на дне, провоцирующий замыкание нижней части сеток – это чаще всего происходит у старых АКБ, выработавших свой ресурс;</li>
    <li>случайное попадание посторонних предметов внутрь, происходящее из-за небрежной работы при техническом обслуживании и зарядке батареи;</li>
    <li>механические повреждения, происходящие из-за резких маневров автомобиля в движении, если батарея плохо закреплена;</li>
    <li>регулярная сильная разрядка, происходящая из-за неисправного генератора;</li>
    <li>нарушения правил сборки устройства на заводе-изготовителе.</li>
</ul>
<p>
     Самый первый сигнал о неисправности – это если заряженный аккумулятор не запускает двигатель.
</p>
<p>
     <b>Другие признаки короткого замыкания:</b>
</p>
<ul class="red">
    <li>снижение плотности электролита;</li>
    <li>падение электрического напряжения;</li>
    <li>изменение нормального тока;</li>
    <li>нагрев корпуса при работе;</li>
    <li>нарушение цепи аккумулятора, быстрая потеря зарядки.</li>
</ul>
<p>
     В старых аккумуляторах возможность КЗ является естественным «возрастным» состоянием.
</p>
<p>
     <b>Для проверки состояния АКБ</b> можно самостоятельно сделать предварительные выводы, если обратить внимание на несколько факторов:
</p>
<ul class="red">
    <li>напряжение резко падает до 9-10 Вольт и ниже при проверке нагрузочной вилкой;</li>
    <li>электролит кипит в замкнутой банке при проверке вилкой (если АКБ не имеет пробок, которые нельзя открутить, чтобы посмотреть на состояние электролита – кипение можно услышать, если создать полную тишину);</li>
    <li>электролит кипит при подаче напряжения в 14 В.</li>
</ul>
<p>
     Если АКБ разряжена, то перед такой проверкой его надо зарядить. Если мостик между пластинами находится в начальной стадии формирования, то быстрой разрядки батареи не произойдет, и такая проверка не выявит признаки КЗ. В этом случае надо обратить внимание на то, как быстро разряжается батарея после полной зарядки в состоянии покоя. Если при неработающем двигателе за 3-4 суток падает напряжение – АКБ неисправна.
</p>
<p>
     <b>Чтобы избежать короткого замыкания, рекомендуется:</b>
</p>
<ul class="red">
    <li>избегать превышения зарядки;</li>
    <li>устанавливать правильное значение тока при зарядке;</li>
    <li>избегать попадания мусора, пыли, посторонних предметов внутрь;</li>
    <li>избегать падения батареи, ударов, опрокидывания набок, переворачивания;</li>
    <li>содержать в чистоте корпус и особенно клеммы;</li>
    <li>регулярно проверять плотность электролита, напряжение, уровень зарядки.</li>
</ul>
<p>
     Если часто происходит перезарядка, то свинцовые пластины могут получить повреждения.
</p>
<p>
     Корпус должен быть ровным и исправным, без вмятин, искривлений и прочих дефектов. Иначе батарея может неожиданно выйти из строя. На корпусе не должно быть пыли, грязи, капелек масла и других рабочих жидкостей. Клеммы со временем начинают окисляться. Их нужно очищать. Крепление должно быть прочным - клеммы должны прилегать к свинцовым наконечникам максимально плотно.
</p>
<h2 class="text-danger-custom text-center text-xl-left">Опасность и последствия для аккумулятора и машины</h2>
<p>
     Если короткое замыкание все-таки произошло, то могут быть следующие <b>последствия</b>:
</p>
<ul class="red">
    <li>полный выход АКБ из строя;</li>
    <li>пожар;</li>
    <li>выход из строя бортового компьютера, сигнализации и всей остальной автомобильной электроники;</li>
    <li>сгорание предохранителей;</li>
    <li>поломка генератора.</li>
</ul>
<p>
     В интернете много советов о том, как восстановить батарею, в которой произошло КЗ. Но большинство специалистов не рекомендуют пытаться самостоятельно выполнять какие-то действия. Это не только бесполезно, но и опасно. Можно повредить автомобиль, можно получить травмы.
</p>
