<h2 class="text-danger-custom text-center text-xl-left">Утилизация старого электролита АКБ</h2>
                                        <p class="pt-0">По сути, аккумуляторный электролит представляет собой раствор серной кислоты, смешанный с дистиллированной водой в определенных пропорциях. Соотношение H2SO4 зависит от региона, в котором будет эксплуатироваться источник питания. В большинстве случаев, в 100% токопроводящей жидкости содержится 35% опасного вещества – этого достаточно для регионов с умеренным климатом, где температура воздуха не понижается до отметки ниже 35°С. Свинцово-кислотные батареи, поставляющиеся в отдаленные регионы нашей страны, содержат до 45% кислоты, что обеспечивает стабильную работу и отсутствие замерзаний при экстремально низких (до -60°С) показателях столбиков уличного термометра.</p>
                                        
                                        <p class="pt-0">При этом в процессе эксплуатации, при нагреве свинцовых пластин, из-за химической реакции, плотность кислоты будет падать, следовательно, все АКБ имеют весьма ограниченный ресурс – от 3 до 5 лет, после чего подлежат обязательной замене с последующей утилизацией. Тем не менее, на рынке можно найти обслуживаемые батареи с возможностью «реанимации» (хотя производители постепенно отходят от такой практики).</p>
                                        <p class="pt-0">Особенностью таких аккумуляторов является разборный корпус: располагая соответствующими инструментами, водитель или автомеханик могут снимать крышку, получая все возможности для десульфатации пластин, замены отработанного электролита, частичного или полного восстановления при утрате ресурса. При этом «сливать» отработанный электролит в землю или канализацию категорически запрещено. Ведь активные вещества токопроводящей жидкости крайне агрессивны, «убивают» живые организмы, а при длительном воздействии приводят к повреждению стальных водопроводных труб.</p>
 <p class="pt-0">Поэтому жидкость из свинцово-кислотных АКБ подлежит утилизации с:</p>
